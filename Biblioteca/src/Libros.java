
public class Libros {
    
    public String Autor;
    public String Fecha;
    public String Titulo;
    public String Descripcion;
    public String Palabras;
    public String Edicion;
    public String Temas;
    public String Copias;
    public String Area;
    public String Categoria;
    public String isbn;

    public Libros(String Autor, String Fecha, String Titulo, String Descripcion, String Palabras, String Edicion, String Temas, String Copias, String Area, String Categoria, String isbn) {
        this.Autor = Autor;
        this.Fecha = Fecha;
        this.Titulo = Titulo;
        this.Descripcion = Descripcion;
        this.Palabras = Palabras;
        this.Edicion = Edicion;
        this.Temas = Temas;
        this.Copias = Copias;
        this.Area = Area;
        this.Categoria = Categoria;
        this.isbn = isbn;
    }

    public String getAutor() {
        return Autor;
    }

    public void setAutor(String Autor) {
        this.Autor = Autor;
    }

    public String getFecha() {
        return Fecha;
    }

    public void setFecha(String Fecha) {
        this.Fecha = Fecha;
    }

    public String getTitulo() {
        return Titulo;
    }

    public void setTitulo(String Titulo) {
        this.Titulo = Titulo;
    }

    public String getDescripcion() {
        return Descripcion;
    }

    public void setDescripcion(String Descripcion) {
        this.Descripcion = Descripcion;
    }

    public String getPalabras() {
        return Palabras;
    }

    public void setPalabras(String Palabras) {
        this.Palabras = Palabras;
    }

    public String getEdicion() {
        return Edicion;
    }

    public void setEdicion(String Edicion) {
        this.Edicion = Edicion;
    }

    public String getTemas() {
        return Temas;
    }

    public void setTemas(String Temas) {
        this.Temas = Temas;
    }

    public String getCopias() {
        return Copias;
    }

    public void setCopias(String Copias) {
        this.Copias = Copias;
    }

    public String getArea() {
        return Area;
    }

    public void setArea(String Area) {
        this.Area = Area;
    }

    public String getCategoria() {
        return Categoria;
    }

    public void setCategoria(String Categoria) {
        this.Categoria = Categoria;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }
    
    
    
 
    
}
